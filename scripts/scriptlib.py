def camel_case(name: str) -> str:
  """Convert snake_case names to CamelCase."""
  return ''.join(part.capitalize() for part in name.split('_'))


def load_module(path: str) -> types.ModuleType:
  logger.debug('Loading module from path %s', path)
  module_name = os.path.splitext(os.path.basename(path))[0] + '_tmp'
  spec = importlib.util.spec_from_file_location(module_name, path)
  if not spec or not spec.loader:
    raise ImportError(f'Could not load spec for module at {path}')
  module = importlib.util.module_from_spec(spec)
  spec.loader.exec_module(module)
  logger.debug('Module %s loaded successfully', module_name)
  return module


def py_to_ts(py_type: Any) -> str:
  origin = get_origin(py_type)
  args = get_args(py_type)
  logger.debug('py_to_ts resolving type=%r origin=%r args=%r', py_type, origin, args)

  # Handle generics: List[X] or Tuple[X, ...]
  if origin in (list, tuple):
    inner_type = py_to_ts(args[0]) if args else 'any'
    result = f'{inner_type}[]'
    logger.debug('py_to_ts mapped list/tuple %r to %s', py_type, result)
    return result

  # Handle Optional[X] (i.e. Union[X, None])
  if origin is Union:
    non_none = [arg for arg in args if arg is not type(None)]
    if len(non_none) == 1:
      result = f'{py_to_ts(non_none[0])} | null'
      logger.debug('py_to_ts mapped optional %r to %s', py_type, result)
      return result
    result = ' | '.join(py_to_ts(arg) for arg in args)
    logger.debug('py_to_ts mapped union %r to %s', py_type, result)
    return result

  # Known primitives
  if py_type in PY_TO_TS:
    result = PY_TO_TS[py_type]
    logger.debug('py_to_ts used primitive map for %r -> %s', py_type, result)
    return result

  # Pydantic models → use interface name
  if isinstance(py_type, type) and issubclass(py_type, BaseModel):
    result = py_type.__name__
    logger.debug('py_to_ts resolved BaseModel %r -> %s', py_type, result)
    return result

  # Fallback
  logger.debug('py_to_ts falling back to any for %r', py_type)
  return 'any'


def model_to_ts(model: type[BaseModel]) -> str:
  logger.debug('Generating TypeScript interface for model %s', model.__name__)
  lines = [f'export interface {model.__name__} {{']
  fields = getattr(model, 'model_fields', None) or getattr(model, '__fields__', {})
  for name, field in fields.items():
    annotation = getattr(field, 'annotation', None) or getattr(field, 'outer_type_', None)
    ts_type = py_to_ts(annotation)
    logger.debug('Field %s annotated with %r mapped to %s', name, annotation, ts_type)
    lines.append(f'  {name}: {ts_type};')
  lines.append('}')
  logger.debug('Completed interface for %s with %d fields', model.__name__, len(fields))
  return '\n'.join(lines)


def parse_version(ver: str) -> tuple[int, int, int, int]:
  """Convert a version string like 'v1.2.3.4' into its numeric parts."""
  ver = ver.lstrip('v')
  major, minor, patch, build = [int(p) for p in ver.split('.')]
  logger.debug('Parsed version %s -> (%d, %d, %d, %d)', ver, major, minor, patch, build)
  return major, minor, patch, build


def next_build(current_version: str, last_version: str) -> int:
  """Return the next build number for the given versions.

  When the major or minor component changes the build counter resets to 0.
  Otherwise the counter is incremented from the current value.
  """
  cur_major, cur_minor, _, cur_build = parse_version(current_version)
  last_major, last_minor, _, _ = parse_version(last_version)
  if (cur_major, cur_minor) != (last_major, last_minor):
    return 0
  return cur_build + 1


def bump_version(version: str, part: str) -> str:
  """Increment the specified part of a version string."""
  ma, mi, pa, bu = parse_version(version)
  match part:
    case 'major':
      ma += 1
      mi = 0
      pa = 0
      bu = 0
    case 'minor':
      mi += 1
      pa = 0
      bu = 0
    case 'patch':
      pa += 1
      bu = 0
    case 'build':
      bu += 1
    case _:
      raise ValueError(f'Unknown part: {part}')
  new_version = f'v{ma}.{mi}.{pa}.{bu}'
  logger.debug('Bumped version %s part %s -> %s', version, part, new_version)
  return new_version


async def _fetch_json(cur):
  parts: list[str] = []
  while True:
    row = await cur.fetchone()
    if not row:
      break
    parts.append(row[0])
  result = json.loads(''.join(parts)) if parts else []
  logger.debug('_fetch_json assembled %d chunks into %d records', len(parts), len(result))
  return result


async def _fetch_dicts(cur):
  rows = await cur.fetchall()
  if not rows:
    logger.debug('_fetch_dicts returned 0 rows')
    return []
  cols = [d[0] for d in cur.description]
  result = [dict(zip(cols, row)) for row in rows]
  logger.debug('_fetch_dicts returning %d rows with columns %s', len(result), cols)
  return result


def _decode_optional(value):
  if value is None:
    return None
  logger.debug('_decode_optional processing value of type %s', type(value).__name__)
  if isinstance(value, memoryview):
    value = value.tobytes()
  if isinstance(value, (bytes, bytearray)):
    return value.decode('utf-16le')
  return value


def _quote(name: str) -> str:
  return '[' + name.replace(']', ']]') + ']'


def _qualify(schema: str, name: str) -> str:
  return f'{_quote(schema)}.{_quote(name)}'


def _map_constraint_type(code: str | None) -> str | None:
  if not code:
    return code
  mapping = {
    'PK': 'PRIMARY KEY',
    'UQ': 'UNIQUE',
    'F': 'FOREIGN KEY',
  }
  return mapping.get(code, code)


async def list_tables(conn):
  async with conn.cursor() as cur:
    await cur.execute(
      "SELECT TABLE_SCHEMA AS table_schema, TABLE_NAME AS table_name "
      "FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_TYPE='BASE TABLE' FOR JSON PATH"
    )
    result = await _fetch_json(cur)
  logger.debug('list_tables discovered %d tables', len(result))
  return result


async def list_views(conn):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(SCHEMA_NAME(v.schema_id) AS NVARCHAR(4000)) AS view_schema,
                CAST(v.name AS NVARCHAR(4000)) AS view_name,
                CAST(m.definition AS NVARCHAR(4000)) AS view_definition
           FROM sys.views v
           JOIN sys.sql_modules m ON v.object_id = m.object_id
          WHERE SCHEMA_NAME(v.schema_id)='dbo'
           FOR JSON PATH"""
    )
    rows = await _fetch_dicts(cur)
  for row in rows:
    row['view_definition'] = _decode_optional(row['view_definition'])
  logger.debug('list_views retrieved %d views', len(rows))
  return rows


async def list_view_dependencies(conn):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(OBJECT_NAME(d.referencing_id) AS NVARCHAR(4000)) AS view_name,
                CAST(OBJECT_NAME(d.referenced_id) AS NVARCHAR(4000)) AS ref_name
           FROM sys.sql_expression_dependencies d
           JOIN sys.views v ON d.referencing_id = v.object_id
           JOIN sys.views r ON d.referenced_id = r.object_id
          WHERE SCHEMA_NAME(v.schema_id)='dbo'
            AND SCHEMA_NAME(r.schema_id)='dbo'
           FOR JSON PATH"""
    )
    result = await _fetch_json(cur)
  logger.debug('list_view_dependencies found %d dependency records', len(result))
  return result


async def list_columns(conn, schema: str, table: str):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(c.name AS NVARCHAR(4000)) AS column_name,
                CAST(t.name AS NVARCHAR(4000)) AS data_type,
                c.max_length,
                c.precision,
                c.scale,
                c.is_nullable,
                CAST(dc.definition AS NVARCHAR(4000)) AS default_definition,
                ic.seed_value,
                ic.increment_value,
                c.is_identity,
                c.is_rowguidcol,
                CAST(cc.definition AS NVARCHAR(4000)) AS computed_definition,
                cc.is_persisted,
                CAST(c.collation_name AS NVARCHAR(4000)) AS collation_name
           FROM sys.columns c
           JOIN sys.types t
             ON c.user_type_id = t.user_type_id
          LEFT JOIN sys.default_constraints dc
             ON c.default_object_id = dc.object_id
          LEFT JOIN sys.identity_columns ic
             ON c.object_id = ic.object_id AND c.column_id = ic.column_id
          LEFT JOIN sys.computed_columns cc
             ON c.object_id = cc.object_id AND c.column_id = cc.column_id
          WHERE c.object_id = OBJECT_ID(?)
          ORDER BY c.column_id""",
      (_qualify(schema, table),),
    )
  rows = await _fetch_dicts(cur)
  columns: list[dict] = []
  for row in rows:
    logger.debug(
      'Column %s.%s.%s type=%s max_length=%s precision=%s scale=%s nullable=%s',
      schema,
      table,
      row['column_name'],
      row['data_type'],
      row['max_length'],
      row['precision'],
      row['scale'],
      row['is_nullable'],
    )
    columns.append(
      {
        'name': row['column_name'],
        'data_type': row['data_type'],
        'max_length': row['max_length'],
        'precision': row['precision'],
        'scale': row['scale'],
        'nullable': bool(row['is_nullable']),
        'default': _decode_optional(row['default_definition']),
        'identity': bool(row['is_identity']),
        'identity_seed': row['seed_value'],
        'identity_increment': row['increment_value'],
        'rowguidcol': bool(row['is_rowguidcol']),
        'computed': _decode_optional(row['computed_definition']),
        'computed_persisted': bool(row['is_persisted']),
        'collation': row['collation_name'],
      }
    )
  logger.debug('list_columns returning %d columns for %s.%s', len(columns), schema, table)
  return columns


async def list_primary_key(conn, schema: str, table: str):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(c.name AS NVARCHAR(4000)) AS column_name
           FROM sys.index_columns ic
           JOIN sys.columns c ON ic.object_id = c.object_id AND ic.column_id = c.column_id
           JOIN sys.indexes i ON ic.object_id = i.object_id AND ic.index_id = i.index_id
          WHERE i.object_id = OBJECT_ID(?) AND i.is_primary_key = 1
          ORDER BY ic.key_ordinal""",
      (_qualify(schema, table),),
    )
    rows = await _fetch_dicts(cur)
  result = [row['column_name'] for row in rows]
  logger.debug('list_primary_key for %s.%s -> %s', schema, table, result)
  return result


async def list_foreign_keys(conn, schema: str, table: str):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(fk.name AS NVARCHAR(4000)) AS constraint_name,
                CAST(COL_NAME(fkc.parent_object_id, fkc.parent_column_id) AS NVARCHAR(4000)) AS column_name,
                CAST(OBJECT_SCHEMA_NAME(fkc.referenced_object_id) AS NVARCHAR(4000)) AS ref_schema,
                CAST(OBJECT_NAME(fkc.referenced_object_id) AS NVARCHAR(4000)) AS ref_table,
                CAST(COL_NAME(fkc.referenced_object_id, fkc.referenced_column_id) AS NVARCHAR(4000)) AS ref_column
           FROM sys.foreign_key_columns fkc
           JOIN sys.foreign_keys fk ON fkc.constraint_object_id = fk.object_id
          WHERE fkc.parent_object_id = OBJECT_ID(?)
          ORDER BY fk.name, fkc.constraint_column_id""",
      (_qualify(schema, table),),
    )
    rows = await _fetch_dicts(cur)
  grouped: dict[str, dict] = {}
  for row in rows:
    entry = grouped.setdefault(
      row['constraint_name'],
      {
        'name': row['constraint_name'],
        'columns': [],
        'ref_columns': [],
        'ref_schema': row['ref_schema'],
        'ref_table': row['ref_table'],
      },
    )
    entry['columns'].append(row['column_name'])
    entry['ref_columns'].append(row['ref_column'])
  result = list(grouped.values())
  logger.debug('list_foreign_keys for %s.%s -> %d constraints', schema, table, len(result))
  return result


async def list_indexes(conn, schema: str, table: str):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(i.name AS NVARCHAR(4000)) AS index_name,
                i.is_unique AS is_unique,
                CAST(i.filter_definition AS NVARCHAR(4000)) AS filter_definition,
                ic.is_descending_key AS is_descending,
                ic.is_included_column AS is_included,
                ic.key_ordinal AS key_ordinal,
                ic.index_column_id AS index_column_id,
                CAST(c.name AS NVARCHAR(4000)) AS column_name
           FROM sys.indexes i
           JOIN sys.index_columns ic
             ON i.object_id = ic.object_id AND i.index_id = ic.index_id
           JOIN sys.columns c
             ON ic.object_id = c.object_id AND ic.column_id = c.column_id
          WHERE i.object_id = OBJECT_ID(?)
            AND i.is_primary_key = 0
            AND i.is_unique_constraint = 0
            AND i.type <> 0
            AND i.is_hypothetical = 0
            AND i.name IS NOT NULL
          ORDER BY i.name,
                   CASE WHEN ic.is_included_column = 0 THEN ic.key_ordinal ELSE ic.index_column_id END""",
      (_qualify(schema, table),),
    )
    rows = await _fetch_dicts(cur)
  indexes: dict[str, dict] = {}
  for row in rows:
    entry = indexes.setdefault(
      row['index_name'],
      {
        'index_name': row['index_name'],
        'is_unique': bool(row['is_unique']),
        'filter_definition': _decode_optional(row['filter_definition']),
        'columns': [],
      },
    )
    entry['columns'].append(
      {
        'column_name': row['column_name'],
        'is_descending': bool(row['is_descending']),
        'is_included': bool(row['is_included']),
      }
    )
  result = list(indexes.values())
  logger.debug('list_indexes for %s.%s -> %d indexes', schema, table, len(result))
  return result


async def list_constraints(conn, schema: str, table: str):
  async with conn.cursor() as cur:
    await cur.execute(
      """SELECT CAST(kc.name AS NVARCHAR(4000)) AS constraint_name,
                CAST(kc.type AS NVARCHAR(4000)) AS constraint_type,
                ic.key_ordinal,
                CAST(c.name AS NVARCHAR(4000)) AS column_name
           FROM sys.key_constraints kc
           JOIN sys.tables t ON kc.parent_object_id = t.object_id
           JOIN sys.schemas s ON t.schema_id = s.schema_id
           LEFT JOIN sys.index_columns ic
             ON kc.parent_object_id = ic.object_id
            AND kc.unique_index_id = ic.index_id
           LEFT JOIN sys.columns c
             ON ic.object_id = c.object_id
            AND ic.column_id = c.column_id
          WHERE s.name=? AND t.name=?
          ORDER BY kc.name, ic.key_ordinal""",
      (schema, table),
    )
    rows = await _fetch_dicts(cur)
  grouped: dict[str, dict] = {}
  order: list[str] = []
  for row in rows:
    name = row['constraint_name']
    if name not in grouped:
      order.append(name)
      grouped[name] = {
        'constraint_name': name,
        'constraint_type': _map_constraint_type(row['constraint_type']),
        'columns': [],
      }
    column = row.get('column_name')
    if column:
      grouped[name]['columns'].append(column)
  results: list[dict] = []
  for name in order:
    entry = grouped[name]
    columns = entry['columns']
    entry['columns'] = ', '.join(columns) if columns else None
    results.append(entry)
  logger.debug('list_constraints for %s.%s -> %d constraints', schema, table, len(results))
  return results


