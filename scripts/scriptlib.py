async def _table_schema(conn, schema_name: str, table: str):
  logger.debug('Building schema snapshot for table %s.%s', schema_name, table)
  columns = await list_columns(conn, schema_name, table)
  primary_key = await list_primary_key(conn, schema_name, table)
  foreign_keys = await list_foreign_keys(conn, schema_name, table)
  indexes = await list_indexes(conn, schema_name, table)
  constraints = await list_constraints(conn, schema_name, table)
  check_constraints = await list_check_constraints(conn, schema_name, table)
  schema = {
    'schema': schema_name,
    'name': _qualify(schema_name, table),
    'table': table,
    'columns': columns,
    'primary_key': primary_key,
    'foreign_keys': foreign_keys,
    'indexes': indexes,
    'constraints': constraints,
    'check_constraints': check_constraints,
  }
  logger.debug(
    'Completed schema snapshot for %s.%s: %d columns, %d FKs, %d indexes',
    schema_name,
    table,
    len(columns),
    len(foreign_keys),
    len(indexes),
  )
  return schema


async def get_schema(conn):
  tables = await list_tables(conn)
  logger.debug('get_schema processing %d tables', len(tables))
  schemas: dict[str, dict] = {}
  deps: dict[str, set[str]] = {}
  for t in tables:
    schema_name = t['table_schema']
    table_name = t['table_name']
    info = await _table_schema(conn, schema_name, table_name)
    key = f'{schema_name}.{table_name}'
    schemas[key] = info
    deps[key] = {
      f"{fk['ref_schema']}.{fk['ref_table']}"
      for fk in info['foreign_keys']
      if fk.get('ref_schema') and fk.get('ref_table')
    }
    logger.debug('Recorded dependencies for %s: %s', key, deps[key])
  ordered: list[str] = []
  visited: set[str] = set()

  def visit(name: str):
    if name in visited:
      return
    visited.add(name)
    for dep in deps.get(name, set()):
      if dep in deps:
        visit(dep)
    ordered.append(name)

  for table_name in deps.keys():
    visit(table_name)
  logger.debug('Table dependency order: %s', ordered)

  view_defs = await list_views(conn)
  view_map = {v['view_name']: v['view_definition'] for v in view_defs}
  logger.debug('get_schema processing %d views', len(view_map))
  view_deps: dict[str, set[str]] = {}
  for dep in await list_view_dependencies(conn):
    view_deps.setdefault(dep['view_name'], set()).add(dep['ref_name'])
  logger.debug('View dependencies: %s', view_deps)
  vordered: list[str] = []
  vvisited: set[str] = set()

  def visit_view(name: str):
    if name in vvisited:
      return
    vvisited.add(name)
    for dep in view_deps.get(name, set()):
      if dep in view_deps:
        visit_view(dep)
    vordered.append(name)

  for view_name in view_map.keys():
    visit_view(view_name)
  logger.debug('View dependency order: %s', vordered)

  schema_snapshot = {
    'tables': [schemas[n] for n in ordered],
    'views': [{'name': n, 'definition': view_map[n]} for n in vordered],
  }
  logger.debug(
    'Schema snapshot prepared with %d tables and %d views',
    len(schema_snapshot['tables']),
    len(schema_snapshot['views']),
  )
  return schema_snapshot


def _format_column(column: dict) -> str:
  ctype = column['data_type']
  length = column.get('max_length')
  precision = column.get('precision')
  scale = column.get('scale')
  computed = column.get('computed')
  if computed:
    definition = computed.strip()
    if not definition.upper().startswith('AS'):
      definition = f'AS {definition}'
    line = f"{_quote(column['name'])} {definition}"
  else:
    if ctype.lower() in {'varchar', 'nvarchar', 'char', 'nchar', 'varbinary', 'binary'}:
      if length == -1:
        ctype = f'{ctype}(MAX)'
      elif length is not None:
        if ctype.lower().startswith('n'):
          length = length // 2 if length else length
        ctype = f'{ctype}({length})'
    elif ctype.lower() in {'decimal', 'numeric'} and precision is not None:
      scale = scale or 0
      ctype = f'{ctype}({precision},{scale})'
    line = f"{_quote(column['name'])} {ctype}"
    if column.get('identity'):
      seed = column.get('identity_seed') or 1
      inc = column.get('identity_increment') or 1
      line += f' IDENTITY({seed},{inc})'
    default = column.get('default')
    if default:
      line += f' DEFAULT {default}'
    if not column.get('nullable', True):
      line += ' NOT NULL'
    if column.get('rowguidcol'):
      line += ' ROWGUIDCOL'
    if column.get('collation'):
      line += f" COLLATE {column['collation']}"
  return line


def _build_create_sql(table: dict) -> str:
  parts: list[str] = []
  for column in table['columns']:
    parts.append(_format_column(column))
  if table['primary_key']:
    pk_cols = ', '.join(_quote(col) for col in table['primary_key'])
    parts.append(f'PRIMARY KEY ({pk_cols})')
  for fk in table.get('foreign_keys', []):
    ref_schema = fk.get('ref_schema')
    ref_table = fk.get('ref_table')
    columns = fk.get('columns') or []
    ref_columns = fk.get('ref_columns') or []
    if not (ref_schema and ref_table and columns and ref_columns):
      continue
    ref = _qualify(ref_schema, ref_table)
    left = ', '.join(_quote(col) for col in columns)
    right = ', '.join(_quote(col) for col in ref_columns)
    clause = f"FOREIGN KEY ({left}) REFERENCES {ref}({right})"
    if fk.get('name'):
      clause = f"CONSTRAINT {_quote(fk['name'])} " + clause
    parts.append(clause)
  for constraint in table.get('constraints', []):
    ctype = (constraint.get('constraint_type') or '').upper()
    name = constraint.get('constraint_name')
    cols = constraint.get('columns')
    if ctype == 'UNIQUE' and cols:
      clause = f"CONSTRAINT {_quote(name)} UNIQUE ({cols})" if name else f"UNIQUE ({cols})"
      parts.append(clause)
  for chk in table.get('check_constraints', []):
    definition = chk.get('definition')
    if not definition:
      continue
    name = chk.get('constraint_name')
    trimmed = definition.strip()
    if trimmed.startswith('(') and trimmed.endswith(')'):
      check_body = trimmed
    else:
      check_body = f'({trimmed})'
    clause = f"CONSTRAINT {_quote(name)} CHECK {check_body}" if name else f"CHECK {check_body}"
    parts.append(clause)
  return f"CREATE TABLE {table['name']} ({', '.join(parts)})"


async def dump_schema(conn, prefix: str = 'schema') -> str:
  schema = await get_schema(conn)
  logger.debug(
    'dump_schema received %d tables and %d views',
    len(schema.get('tables', [])),
    len(schema.get('views', [])),
  )
  ts = datetime.now(timezone.utc).strftime('%Y%m%d')
  prefix_path = Path(prefix)
  base_name = prefix_path.name
  if prefix_path.is_absolute():
    target_dir = prefix_path.parent
  elif prefix_path.parent != Path('.'):
    target_dir = (ROOT / prefix_path.parent).resolve()
  else:
    target_dir = Path(__file__).resolve().parent
  target_dir.mkdir(parents=True, exist_ok=True)
  filename = f'{base_name}_{ts}.sql'
  out_path = target_dir / filename
  logger.debug('Writing schema dump to %s', out_path)
  lines: list[str] = []
  for table in schema['tables']:
    logger.debug('Serializing table %s', table['name'])
    lines.append(_build_create_sql(table) + ';')
    for idx in table.get('indexes', []):
      columns: list[str] = []
      included: list[str] = []
      for col in idx.get('columns', []):
        name = _quote(col['column_name'])
        if col.get('is_included'):
          included.append(name)
          continue
        direction = ' DESC' if col.get('is_descending') else ''
        columns.append(f'{name}{direction}')
      if not columns:
        continue
      statement = 'CREATE '
      if idx.get('is_unique'):
        statement += 'UNIQUE '
      statement += f"INDEX {idx['index_name']} ON {table['name']} ({', '.join(columns)})"
      if included:
        statement += f" INCLUDE ({', '.join(included)})"
      if idx.get('filter_definition'):
        statement += f" WHERE {idx['filter_definition']}"
      lines.append(statement + ';')
      logger.debug('Added index statement: %s', statement)
  for view in schema.get('views', []):
    raw_def = re.sub(r'--.*?(\r?\n|$)', ' ', view['definition'] or '')
    definition = ' '.join(raw_def.split())
    if not definition:
      continue
    if not definition.lower().startswith('create'):
      definition = f"CREATE VIEW {view['name']} AS {definition}"
    if not definition.endswith(';'):
      definition += ';'
    lines.append(definition)
    logger.debug('Added view definition for %s', view['name'])
  with open(out_path, 'w', encoding='utf-8') as fh:
    fh.write('\n'.join(lines))
  try:
    rel_path = out_path.relative_to(ROOT)
    result = rel_path.as_posix()
  except ValueError:
    result = str(out_path)
  print(f'Schema dumped to {result}')
  logger.debug('Schema dump complete: %s', result)
  return result


async def dump_data(conn, prefix: str = 'dump_data') -> str:
  schema = await get_schema(conn)
  logger.debug('dump_data processing %d tables', len(schema.get('tables', [])))
  data: dict[str, list[dict]] = {}
  for table in schema['tables']:
    async with conn.cursor() as cur:
      await cur.execute(f"SELECT * FROM {table['name']} FOR JSON PATH")
      rows = await _fetch_json(cur)
      data[table['name']] = rows
      logger.debug('dump_data captured %d rows for %s', len(rows), table['name'])
  ts = datetime.now(timezone.utc).strftime('%Y%m%d_BACKUP')
  filename = f'{prefix}_{ts}.json'
  with open(filename, 'w', encoding='utf-8') as fh:
    json.dump({'schema': schema, 'data': data}, fh, indent=2, default=str)
  print(f'Data dumped to {filename}')
  logger.debug('Data dump complete: %s', filename)
  return filename


async def apply_schema(conn, path: str):
  logger.debug('Applying schema from %s', path)
  with open(path, 'r', encoding='utf-8') as fh:
    sql = fh.read()
  async with conn.cursor() as cur:
    executed = 0
    for statement in sql.split(';'):
      stmt = statement.strip()
      if not stmt:
        continue
      logger.debug('Executing schema statement %d: %s', executed + 1, stmt[:120])
      await cur.execute(stmt)
      executed += 1
  logger.debug('Applied %d statements from %s', executed, path)
  print('Schema applied.')


async def connect(dbname: str | None = None):
  logger.debug('connect requested for database override %s', dbname)
  try:
    import aioodbc  # type: ignore
  except Exception as exc:
    raise ImportError('aioodbc is required for database operations') from exc
  dsn = os.getenv('AZURE_SQL_CONNECTION_STRING')
  if not dsn:
    raise RuntimeError('AZURE_SQL_CONNECTION_STRING not set')
  if dbname:
    parts = []
    replaced = False
    for part in dsn.split(';'):
      upper = part.upper()
      if upper.startswith('DATABASE=') or upper.startswith('INITIAL CATALOG='):
        parts.append(f'DATABASE={dbname}')
        replaced = True
      else:
        parts.append(part)
    if not replaced:
      parts.append(f'DATABASE={dbname}')
    dsn = ';'.join(parts)
  logger.debug('Establishing aioodbc connection (override applied=%s)', bool(dbname))
  conn = await aioodbc.connect(dsn=dsn, autocommit=True)
  print(f'Connected to database {dbname or dsn}')
  logger.debug('Connection established successfully')
  return conn
